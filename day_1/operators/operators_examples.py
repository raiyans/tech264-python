# Addition
x = 5
y = 1
addition = x + y
print("Addition:", addition)

# Subtraction
subtraction = x - y
print("Subtraction:", subtraction)

# Multiplication
multiplication = x * y
print("Multiplication:", multiplication)

# Division
division = x / y
print("Division:", division)

# Remainder (Modulus)
remainder = x % y
print("Remainder:", remainder)

# Greater than
is_greater = x > y
print("Is x greater than y?", is_greater)

# Less than
is_less = x < y
print("Is x less than y?", is_less)

# Check if equal
is_equal = x == y
print("Is x equal to y?", is_equal)

# Check if not equal
is_not_equal = x != y
print("Is x not equal to y?", is_not_equal)

# Greater than or equal to
is_greater_or_equal = x >= y
print("Is x greater than or equal to y?", is_greater_or_equal)

# Less than or equal to
is_less_or_equal = x <= y
print("Is x less than or equal to y?", is_less_or_equal)